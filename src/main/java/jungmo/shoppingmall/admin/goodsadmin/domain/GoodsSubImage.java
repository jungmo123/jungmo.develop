package jungmo.shoppingmall.admin.goodsadmin.domain;

public class GoodsSubImage {
	private String subImageUrl;
	private int godNum;
	private int imgNum;
	
	public String getSubImageUrl() {
		return subImageUrl;
	}
	public int getGodNum() {
		return godNum;
	}
	public int getImgNum() {
		return imgNum;
	}
	public void setSubImageUrl(String subImageUrl) {
		this.subImageUrl = subImageUrl;
	}
	public void setGodNum(int godNum) {
		this.godNum = godNum;
	}
	public void setImgNum(int imgNum) {
		this.imgNum = imgNum;
	}
}
